import pandas as pd
import numpy as np

df = pd.read_excel("超市销售数据.xlsx")
print(df.head())
print(df.dtypes)
# 题目 1：复杂读取与多层筛查
# 要求：
# 1.	读取 Excel，并删除所有全为空的行。
df = df.dropna(how='all')
# 2.	把所有中文字段前后的空格、不可见字符全部清除。
str_cols = ['省份','城市','商品类别','支付方式']
for col in str_cols:
    df[col] = df[col].astype(str).str.strip()
# 3.	将 'nan'、'None'、'无' 统一替换为缺失值 NaN。
df = df.replace(['nan','None','无'],np.nan)
# 4.	只保留 销售额 > 0 且 销量≥1 且 省份不为空 的记录。
df = df[(df['销售额']>0) & (df['销量']>=1) & (df['省份'].notna())]
print(df.head(15))


# 题目 2：按规则批量填充缺失值（进阶版）
# 要求：
# 1.	城市的缺失值：按「省份」分组，用该省出现次数最多的城市填充。
# df['省份'].dropna().unique() 提取出不同省份，去掉空值，选取唯一值
for prov in (df['省份'].dropna().unique()):
    most_city = df[df['省份'] == prov]['城市'].mode()[0]
    # 省份是某省，城市是空值
    df.loc[(df['省份'] == prov) & (df['城市'].isna()),'城市'] = most_city
    print(df.head(15))

# 2.	商品类别缺失：用「同省份 + 同月份」的众数填充。
# 调用时间函数对df['日期']使用dt方法提取月份
df['月份'] = pd.to_datetime(df['日期']).dt.month
# 取出对应的分组【取出的列。做对应变换
df['商品类别'] = df.groupby(['省份','月份'])['商品类别'].transform(lambda x: x.fillna(x.mode()[0]))
print(df.head(20))

# 3.	销售额缺失：按「商品类别 + 支付方式」分组均值填充。
df['销售额'] = df.groupby(['商品类别','支付方式'])['销售额'].transform(lambda x: x.fillna(x.mode()))
# 4.	销量缺失：按「省份 + 城市」分组中位数填充。
#
# 题目 3：重复数据的智能去重
# 要求：
# 1.	删除完全重复行。
df.drop_duplicates(inplace = True)
# 2.	对满足以下条件的视为重复记录，只保留销售额最大的那条：
# o	省份相同 + 城市相同 + 日期相同 + 商品类别相同
df = df.sort_values('销售额',ascending= False).drop_duplicates(subset = ['省份',"城市","日期","商品类别"],
                                                       keep='first')
# 3.	最后重置索引。
df.reset_index(drop = True)
print(df.head())

# 题目 4：异常值清洗与修正（综合逻辑）
# 要求：
# 1.	销售额 < 0 的，修正为绝对值。
df['销售额'] = df['销售额'].abs()

# 2.	销售额 > 50000 的，替换为该商品类别的 95 分位数。
for lb in df['商品类别'].unique():
    # 取出某个便利中的类别 去取出销售额 95分位数
    # 分位数：某组数据从小到大排序在n位置上的数
    Q95 = df[df['商品类别'] == lb]['销售额'].quantile(0.95)
    df.loc[(df['商品类别'] == lb) & (df['销售额'] > 50000),'销售额'] = Q95
# 3.	销量 > 200 的，强制设为 200。
df.loc[df['销量'] > 200, '销量'] = 200

# 4.	日期不在 2023 年的记录全部删除。
df = df[df['日期'].dt.year ==2023]
# 题目 5：字段工程（多列计算 + 分列 + 合并）
# 要求：
# 1.	从「日期」中拆分出：年份、季度、月份、星期几。
df['年份'] = df['日期'].dt.year
df['月份'] = df['日期'].dt.month
df['季度'] = df['日期'].dt.quarter
# 2.	新增字段：客单价 = 销售额 / 销量（保留 2 位小数）。
# 3.	新增字段：销售规模，规则：
# o	销售额 ≥ 10000 → ‘大额’
# o	3000 ≤ 销售额 < 10000 → ‘中额’
# o	其他 → ‘小额’
# 4.	将「商品类别」中的 “零食”“ 零食” 等不规范值统一为 “零食”，同理规整所有类别。
#
# 题目 6：多层分组 + 多指标聚合（综合统计）
# 要求：
# 按 省份 + 季度 + 商品类别 分组，统计：
# 1.	销售额：总金额、均值、最大值、最小值
# 2.	销量：总销量、平均销量
# 3.	订单数：计数
# 4.	客单价：平均客单价
# 结果保留 2 位小数。
#
# 题目 7：透视表 + 多层索引（必考难点）
# 要求：
# 1.	做透视表：
# o	行 = 省份
# o	列 = 季度
# o	值 = 销售额
# o	聚合 = 求和
# 2.	将透视表stack 转为层次化 Series。
# 3.	再 unstack 恢复，并计算每个省份季度环比增长率。
# 4.	缺失的环比值填 0。
#
# 题目 8：条件筛选 + 布尔索引（复杂逻辑）
# 要求：
# 筛选出满足全部以下条件的数据：
# 1.	福建省或广东省
# 2.	季度为 2 季度或 3 季度
# 3.	商品类别为 “零食” 或 “生鲜”
# 4.	销售额 > 该城市平均销售额
# 5.	销量 > 该类别中位数销量
#
# 题目 9：数据规整（宽表 ↔ 长表 互转 + 合并）
# 要求：
# 1.	构建一个宽表：
# o	行：省份
# o	列：月份
# o	值：销售额总和
# 2.	将宽表转为长表（melt）。
# 3.	再将长表与「省份总销量表」按省份合并（左连接）。
# 4.	最后计算「当月销售额占省份总销量比例」。
#
# 题目 10：最终输出标准化文件
# 要求：
# 1.	只保留以下字段：
# 省份、城市、日期、季度、商品类别、销售额、销量、客单价、销售规模、支付方式
# 2.	按「日期 + 销售额」降序排序。
# 3.	输出为 清洗完成版_最终版.xlsx
# o	不带索引
# o	编码 utf-8
# o	小数统一 2 位
